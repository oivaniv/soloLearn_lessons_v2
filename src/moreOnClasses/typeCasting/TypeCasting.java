package moreOnClasses.typeCasting;

public class TypeCasting {
    public static void main(String[] args) {
        //Here we initialize the variable a and assign the value, value isn't correct for this variable type so
        //we use type casting to change it to int
        int a = (int) 3.14;
        System.out.println(a);

        double b=4.55;
        int c = (int) b;

    }

}
